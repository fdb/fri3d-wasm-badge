//! Bitmap fonts for text rendering
//!
//! These fonts match the u8g2 library fonts used in the original C implementation:
//! - Primary: u8g2_font_6x10_tf (6 pixels wide, 10 pixels tall)
//! - Secondary: u8g2_font_5x7_tf (5 pixels wide, 7 pixels tall)

/// Bitmap font descriptor
pub struct BitmapFont {
    /// Character width in pixels
    pub width: u8,
    /// Character height in pixels
    pub height: u8,
    /// Baseline offset from top
    pub baseline: u8,
    /// First ASCII character in font
    pub first_char: u8,
    /// Last ASCII character in font
    pub last_char: u8,
}

impl BitmapFont {
    /// Get glyph rows for a character
    pub fn get_glyph(&self, ch: char) -> Option<&'static [u8]> {
        let code = ch as u32;
        if code < self.first_char as u32 || code > self.last_char as u32 {
            return None;
        }

        let index = (code - self.first_char as u32) as usize;

        if self.width == 6 && self.height == 10 {
            if index < FONT_6X10_ROWS.len() {
                Some(&FONT_6X10_ROWS[index])
            } else {
                None
            }
        } else if self.width == 5 && self.height == 7 {
            if index < FONT_5X7_ROWS.len() {
                Some(&FONT_5X7_ROWS[index])
            } else {
                None
            }
        } else {
            None
        }
    }
}

